package Repeticao;

public class ExemploLacoFor {
    public static void main(String[] Arg){

        for(int contador = 1; contador <= 10; contador++){

            System.out.println("Contando: " + contador);
        }

        for(int i = 10; i <= 10; i--){

            System.out.println("Contando Regressivo: " + i);
        }

    }
}
